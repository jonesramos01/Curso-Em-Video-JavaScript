/*VAR, LET, CONST*/

var num1 = 4;
let num2 = 5;
const num3 = 6;

console.log(num1);
console.log(num2);
console.log(num3);

/*
var → pode ser reatribuída (global - acessada de qualquer lugar do código)
let → pode ser reatribuida (local: funciona somente a nível de bloco aonde a mesma foi criada)
const → pode ser modificada (conteúdo), mas não pode ser reatribuida (valor)
*/
